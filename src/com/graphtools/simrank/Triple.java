package com.graphtools.simrank;

public class Triple {
	private int vid;
	private int step;
	private double prob;
	
	public Triple(int vid, int step, double prob){
		this.vid = vid;
		this.step = step;
		this.prob = prob;
	}
	
	public int getVid(){
		return vid;
	}
	
	public int getStep(){
		return step;
	}
	
	public double getProb(){
		return prob;
	}
}
